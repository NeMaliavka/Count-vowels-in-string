# Этот вариант использует цикл для перебора каждого символа в строке и условный оператор для проверки, является ли символ гласной.

def count_vowels(input_string):
    vowels = "aeiouAEIOU"  # Список гласных
    count = 0  # Инициализация счетчика гласных

    # Перебор каждого символа в строке
    for char in input_string:
        if char in vowels:  # Проверка, является ли символ гласной
            count += 1  # Увеличиваем счетчик, если символ гласная

    return count  # Возвращаем общее количество гласных


# Ввод строки от пользователя
user_input = input("Введите строку: ")
result = count_vowels(user_input)  # Вызов функции для подсчета гласных
print(f"Количество гласных: {result}")  # Вывод результата